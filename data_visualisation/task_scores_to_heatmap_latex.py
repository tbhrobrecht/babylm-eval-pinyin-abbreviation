#!/usr/bin/env python3
"""Export task_scores.csv as a rank-colored LaTeX heatmap table.

Colors are assigned independently within each task column. The highest score
receives the darkest shade, and lower-scoring models receive progressively
lighter shades. This makes comparisons sensible even when task difficulties
and raw accuracy ranges differ.

Examples:
    python data_visualisation/task_scores_to_heatmap_latex.py
    python data_visualisation/task_scores_to_heatmap_latex.py --exclude-model benchmark
    python data_visualisation/task_scores_to_heatmap_latex.py --show-stderr
"""

from __future__ import annotations

import argparse
from pathlib import Path

from task_scores_to_latex import (
    Score,
    default_caption,
    formatted_cell,
    included_tasks,
    latex_escape,
    load_scores,
    parse_label_overrides,
    task_label,
)
from visualisation.model_labels import MODEL_LABELS, model_sort_key


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Convert task_scores.csv into a rank-colored LaTeX heatmap table."
    )
    parser.add_argument(
        "--input",
        type=Path,
        default=Path("data_visualisation/output/task_scores.csv"),
        help="Input CSV generated by plot_results.py.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path("data_visualisation/output/task_scores_by_model_heatmap.tex"),
        help="Destination .tex file.",
    )
    parser.add_argument("--metric", default="acc", help="Metric rows to include from the input CSV.")
    parser.add_argument(
        "--show-stderr",
        action="store_true",
        help="Format cells as mean +/- standard error instead of mean alone.",
    )
    parser.add_argument(
        "--include-zhoblimp-subtasks",
        action="store_true",
        help="Include individual ZhoBLiMP diagnostic subtasks in addition to aggregate tasks.",
    )
    parser.add_argument(
        "--include-summary",
        action="store_true",
        help="Include the zeroshot_zho aggregate row when it is present.",
    )
    parser.add_argument(
        "--raw-values",
        action="store_true",
        help="Print values on the 0--1 scale; by default accuracy values are percentages.",
    )
    parser.add_argument(
        "--exclude-model",
        action="append",
        default=[],
        metavar="MODEL",
        help="Omit a model from the table and from ranking; may be supplied multiple times.",
    )
    parser.add_argument(
        "--model-label",
        action="append",
        default=[],
        metavar="MODEL=LABEL",
        help="Override a model's displayed name; may be supplied multiple times.",
    )
    parser.add_argument("--caption", default=None, help="Override the generated LaTeX table caption.")
    parser.add_argument("--label", default="tab:task-scores-by-model-heatmap", help="LaTeX cross-reference label.")
    parser.add_argument(
        "--color",
        default="blue",
        help="xcolor-compatible color name used for heatmap shading (default: blue).",
    )
    parser.add_argument(
        "--min-shade",
        type=int,
        default=8,
        help="Color percentage for the lowest rank (default: 8).",
    )
    parser.add_argument(
        "--max-shade",
        type=int,
        default=65,
        help="Color percentage for the highest rank (default: 65).",
    )
    parser.add_argument(
        "--white-text-threshold",
        type=int,
        default=60,
        help="Use white text in cells shaded at least this dark (default: 60).",
    )
    return parser.parse_args()


def filter_models(
    scores: dict[str, dict[str, Score]], excluded_models: set[str]
) -> dict[str, dict[str, Score]]:
    return {
        task: {model: score for model, score in task_scores.items() if model not in excluded_models}
        for task, task_scores in scores.items()
    }


def shade_by_task(
    scores: dict[str, dict[str, Score]], tasks: list[str], min_shade: int, max_shade: int
) -> dict[str, dict[str, int]]:
    shades: dict[str, dict[str, int]] = {}
    for task in tasks:
        unique_values = sorted({score.mean for score in scores[task].values()})
        if len(unique_values) == 1:
            shades[task] = {model: max_shade for model in scores[task]}
            continue
        shade_for_value = {
            value: round(min_shade + index * (max_shade - min_shade) / (len(unique_values) - 1))
            for index, value in enumerate(unique_values)
        }
        shades[task] = {
            model: shade_for_value[score.mean] for model, score in scores[task].items()
        }
    return shades


def render_table(
    scores: dict[str, dict[str, Score]],
    tasks: list[str],
    model_labels: dict[str, str],
    caption: str,
    label: str,
    show_stderr: bool,
    raw_values: bool,
    color: str,
    min_shade: int,
    max_shade: int,
    white_text_threshold: int,
) -> str:
    models = sorted({model for task in tasks for model in scores[task]}, key=model_sort_key)
    if not models:
        raise SystemExit("No model scores remain after task filtering.")

    best_by_task = {
        task: max((score.mean for score in scores[task].values()), default=None)
        for task in tasks
    }
    shades = shade_by_task(scores, tasks, min_shade, max_shade)
    columns = "l" + "r" * len(tasks)
    lines = [
        r"% Requires \usepackage{booktabs}, \usepackage{graphicx}, and \usepackage[table]{xcolor}.",
        r"\begin{table*}[t]",
        r"\centering",
        rf"\caption{{{caption}}}",
        rf"\label{{{latex_escape(label)}}}",
        r"\resizebox{\textwidth}{!}{%",
        rf"\begin{{tabular}}{{{columns}}}",
        r"\toprule",
        "Model & " + " & ".join(latex_escape(task_label(task)) for task in tasks) + r" \\",
        r"\midrule",
    ]
    for model in models:
        cells: list[str] = []
        for task in tasks:
            score = scores[task].get(model)
            value = formatted_cell(score, best_by_task[task], show_stderr, raw_values)
            if score is not None:
                shade = shades[task][model]
                if shade >= white_text_threshold:
                    value = rf"\textcolor{{white}}{{{value}}}"
                value = rf"\cellcolor{{{color}!{shade}}}{value}"
            cells.append(value)
        lines.append(f"{latex_escape(model_labels.get(model, model))} & " + " & ".join(cells) + r" \\")
    lines.extend([r"\bottomrule", r"\end{tabular}%", r"}", r"\end{table*}", ""])
    return "\n".join(lines)


def default_heatmap_caption(show_stderr: bool, raw_values: bool) -> str:
    return (
        default_caption(show_stderr, raw_values).removesuffix(
            "The best result for each task is shown in bold."
        )
        + "Darker cells indicate a higher rank within each task column; the best result is bold."
    )


def main() -> None:
    args = parse_args()
    if not 0 <= args.min_shade <= args.max_shade <= 100:
        raise SystemExit("--min-shade and --max-shade must satisfy 0 <= min <= max <= 100.")
    if not 0 <= args.white_text_threshold <= 100:
        raise SystemExit("--white-text-threshold must satisfy 0 <= threshold <= 100.")
    scores = filter_models(load_scores(args.input, args.metric), set(args.exclude_model))
    tasks = included_tasks(scores, args.include_zhoblimp_subtasks, args.include_summary)
    table = render_table(
        scores=scores,
        tasks=tasks,
        model_labels={**MODEL_LABELS, **parse_label_overrides(args.model_label)},
        caption=args.caption or default_heatmap_caption(args.show_stderr, args.raw_values),
        label=args.label,
        show_stderr=args.show_stderr,
        raw_values=args.raw_values,
        color=args.color,
        min_shade=args.min_shade,
        max_shade=args.max_shade,
        white_text_threshold=args.white_text_threshold,
    )
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(table, encoding="utf-8")
    print(f"Wrote rank-colored LaTeX heatmap with {len(tasks)} task(s) to {args.output}")


if __name__ == "__main__":
    main()
