#!/usr/bin/env python3
"""Export task_scores.csv as a publication-ready LaTeX results table.

The default table corresponds to the broad task-by-model comparison figure:
aggregate task rows are included, while individual ZhoBLiMP diagnostic
subtasks and the redundant ``zeroshot_zho`` aggregate are omitted.

Examples:
    python data_visualisation/task_scores_to_latex.py
    python data_visualisation/task_scores_to_latex.py --show-stderr
    python data_visualisation/task_scores_to_latex.py \
        --model-label "10k_initials_gpu1=INITIALS (10M)"
"""

from __future__ import annotations

import argparse
import csv
from collections import defaultdict
from dataclasses import dataclass
from pathlib import Path

from visualisation.model_labels import MODEL_LABELS, model_sort_key


DEFAULT_TASK_ORDER = [
    "hellaswag_zh_mubench",
    "winogrande_zh_mubench",
    "xcomps_zh",
    "xstorycloze_zh_mubench",
    "zhoblimp",
]

DEFAULT_TASK_LABELS = {
    "hellaswag_zh_mubench": "HellaSwag (zh)",
    "winogrande_zh_mubench": "WinoGrande (zh)",
    "xcomps_zh": "XCOMPS (zh)",
    "xstorycloze_zh_mubench": "XStoryCloze (zh)",
    "zeroshot_zho": "Zero-shot Chinese average",
    "zhoblimp": "ZhoBLiMP",
}


@dataclass(frozen=True)
class Score:
    mean: float
    stderr: float | None


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Convert task_scores.csv into an academic LaTeX table.")
    parser.add_argument(
        "--input",
        type=Path,
        default=Path("data_visualisation/output/task_scores.csv"),
        help="Input CSV generated by plot_results.py.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path("data_visualisation/output/task_scores_by_model.tex"),
        help="Destination .tex file.",
    )
    parser.add_argument("--metric", default="acc", help="Metric rows to include from the input CSV.")
    parser.add_argument(
        "--show-stderr",
        action="store_true",
        help="Format cells as mean +/- standard error instead of mean alone.",
    )
    parser.add_argument(
        "--include-zhoblimp-subtasks",
        action="store_true",
        help="Include all individual ZhoBLiMP diagnostic subtasks in addition to aggregate tasks.",
    )
    parser.add_argument(
        "--include-summary",
        action="store_true",
        help="Include the zeroshot_zho aggregate row when it is present.",
    )
    parser.add_argument(
        "--raw-values",
        action="store_true",
        help="Print values on the 0--1 scale; by default accuracy values are reported as percentages.",
    )
    parser.add_argument(
        "--model-label",
        action="append",
        default=[],
        metavar="MODEL=LABEL",
        help="Override a model's displayed name; may be supplied multiple times.",
    )
    parser.add_argument(
        "--caption",
        default=None,
        help="Override the generated LaTeX table caption.",
    )
    parser.add_argument("--label", default="tab:task-scores-by-model", help="LaTeX cross-reference label.")
    return parser.parse_args()


def parse_label_overrides(values: list[str]) -> dict[str, str]:
    labels: dict[str, str] = {}
    for value in values:
        model, separator, label = value.partition("=")
        if not separator or not model or not label:
            raise SystemExit(f"Invalid --model-label value '{value}'; expected MODEL=LABEL.")
        labels[model] = label
    return labels


def load_scores(path: Path, metric: str) -> dict[str, dict[str, Score]]:
    required = {"model", "task", "metric", "mean_score", "stderr"}
    scores: dict[str, dict[str, Score]] = defaultdict(dict)
    with path.open(newline="", encoding="utf-8") as handle:
        reader = csv.DictReader(handle)
        missing = required - set(reader.fieldnames or [])
        if missing:
            raise SystemExit(f"{path} is missing required column(s): {', '.join(sorted(missing))}.")
        for row in reader:
            if row["metric"] != metric:
                continue
            stderr_text = row["stderr"].strip()
            scores[row["task"]][row["model"]] = Score(
                mean=float(row["mean_score"]),
                stderr=float(stderr_text) if stderr_text else None,
            )
    if not scores:
        raise SystemExit(f"No rows for metric '{metric}' found in {path}.")
    return dict(scores)


def included_tasks(
    scores: dict[str, dict[str, Score]],
    include_zhoblimp_subtasks: bool,
    include_summary: bool,
) -> list[str]:
    tasks = [task for task in DEFAULT_TASK_ORDER if task in scores]
    if include_summary and "zeroshot_zho" in scores:
        tasks.append("zeroshot_zho")
    extra_tasks = sorted(
        task
        for task in scores
        if task not in tasks
        and task != "zeroshot_zho"
        and (include_zhoblimp_subtasks or not task.startswith("zhoblimp_"))
    )
    return tasks + extra_tasks


def latex_escape(text: str) -> str:
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
        "~": r"\textasciitilde{}",
        "^": r"\textasciicircum{}",
    }
    return "".join(replacements.get(character, character) for character in text)


def task_label(task: str) -> str:
    if task in DEFAULT_TASK_LABELS:
        return DEFAULT_TASK_LABELS[task]
    return task.removeprefix("zhoblimp_").replace("_", " ").title()


def formatted_cell(
    score: Score | None,
    best: float | None,
    show_stderr: bool,
    raw_values: bool,
) -> str:
    if score is None:
        return "--"
    multiplier = 1.0 if raw_values else 100.0
    digits = 3 if raw_values else 2
    mean = f"{score.mean * multiplier:.{digits}f}"
    if show_stderr and score.stderr is not None:
        value = f"${mean} \\pm {score.stderr * multiplier:.{digits}f}$"
    else:
        value = mean
    if best is not None and abs(score.mean - best) < 1e-12:
        return rf"\textbf{{{value}}}"
    return value


def render_table(
    scores: dict[str, dict[str, Score]],
    tasks: list[str],
    model_labels: dict[str, str],
    caption: str,
    label: str,
    show_stderr: bool,
    raw_values: bool,
) -> str:
    models = sorted({model for task in tasks for model in scores[task]}, key=model_sort_key)
    if not models:
        raise SystemExit("No model scores remain after task filtering.")

    best_by_task = {
        task: max((score.mean for score in scores[task].values()), default=None)
        for task in tasks
    }
    columns = "l" + "r" * len(tasks)
    lines = [
        r"% Requires \usepackage{booktabs} and \usepackage{graphicx}.",
        r"\begin{table*}[t]",
        r"\centering",
        rf"\caption{{{caption}}}",
        rf"\label{{{latex_escape(label)}}}",
        r"\resizebox{\textwidth}{!}{%",
        rf"\begin{{tabular}}{{{columns}}}",
        r"\toprule",
        "Model & " + " & ".join(latex_escape(task_label(task)) for task in tasks) + r" \\",
        r"\midrule",
    ]
    for model in models:
        cells = [
            formatted_cell(scores[task].get(model), best_by_task[task], show_stderr, raw_values)
            for task in tasks
        ]
        lines.append(f"{latex_escape(model_labels.get(model, model))} & " + " & ".join(cells) + r" \\")
    lines.extend(
        [
            r"\bottomrule",
            r"\end{tabular}%",
            r"}",
            r"\end{table*}",
            "",
        ]
    )
    return "\n".join(lines)


def default_caption(show_stderr: bool, raw_values: bool) -> str:
    scale = "Accuracy on a 0--1 scale" if raw_values else r"Accuracy (\%)"
    uncertainty = " Values are reported as mean $\\pm$ standard error." if show_stderr else ""
    return (
        f"{scale} on Chinese-language evaluation tasks.{uncertainty} "
        "The best result for each task is shown in bold."
    )


def main() -> None:
    args = parse_args()
    scores = load_scores(args.input, args.metric)
    tasks = included_tasks(scores, args.include_zhoblimp_subtasks, args.include_summary)
    table = render_table(
        scores=scores,
        tasks=tasks,
        model_labels={**MODEL_LABELS, **parse_label_overrides(args.model_label)},
        caption=args.caption or default_caption(args.show_stderr, args.raw_values),
        label=args.label,
        show_stderr=args.show_stderr,
        raw_values=args.raw_values,
    )
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(table, encoding="utf-8")
    print(f"Wrote LaTeX table with {len(tasks)} task(s) to {args.output}")


if __name__ == "__main__":
    main()
